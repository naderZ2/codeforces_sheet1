#include <iostream>

using namespace std;

int main() {
	// Declare variables to store the input values.
	int i;
	long long ll;
	char c;
	float f;
	double d;

	// Read the input values from the console.
	cin >> i >> ll >> c >> f >> d;

	// Print the input values on new lines, without extra spaces.
	cout << i << endl;
	cout << ll << endl;
	cout << c << endl;
	cout << f << endl;
	cout << d << endl;

	return 0;
}
