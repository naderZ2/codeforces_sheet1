#include <iostream>

using namespace std;

int main() {
    // Declare variables to store the input values.
    int a, b;

    // Read the input values from the console.
    cin >> a >> b;

    // Check if A is greater than or equal to B.
    if (a >= b) {
        cout << "Yes" << endl;
    }
    else {
        cout << "No" << endl;
    }

    return 0;
}
